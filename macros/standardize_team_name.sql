{% macro standardize_team_name(column_name) %}
    case lower(trim({{ column_name }}))
        when 'united states' then 'usa'
        when 'united states of america' then 'usa'
        when 'u.s.a.' then 'usa'
        when 'south korea' then 'korea republic'
        when 'korea, republic of' then 'korea republic'
        when 'korea republic' then 'korea republic'
        when 'iran' then 'iran'
        when 'ir iran' then 'iran'
        when 'ivory coast' then 'cote divoire'
        when 'cote divoire' then 'cote divoire'
        when 'dr congo' then 'congo dr'
        when 'democratic republic of congo' then 'congo dr'
        when 'congo dr' then 'congo dr'
        when 'czech republic' then 'czechia'
        when 'czechoslovakia' then 'czechia'
        when 'czechia' then 'czechia'
        when 'turkey' then 'turkiye'
        when 'turkiye' then 'turkiye'
        when 'bosnia-herzegovina' then 'bosnia and herzegovina'
        when 'bosnia & herzegovina' then 'bosnia and herzegovina'
        when 'bosnia and herzegovina' then 'bosnia and herzegovina'
        when 'west germany' then 'germany'
        when 'east germany' then 'germany'
        when 'serbia and montenegro' then 'serbia'
        when 'yugoslavia' then 'serbia'
        when 'fyr macedonia' then 'north macedonia'
        when 'macedonia' then 'north macedonia'
        when 'cape verde' then 'cabo verde'
        when 'cape verde islands' then 'cabo verde'
        when 'curaçao' then 'curacao'
        else lower(trim({{ column_name }}))
    end
{% endmacro %}